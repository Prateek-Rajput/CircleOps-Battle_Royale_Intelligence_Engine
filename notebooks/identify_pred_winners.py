# File: notebooks/identify_predicted_winners.py

import pandas as pd



# â”€â”€â”€ Load Data â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
PRED_PATH   = "data/processed/pubg_phase6_test_predictions.csv"
MATCH_PATH  = "data/processed/test_phase1_matches.csv"

preds   = pd.read_csv(PRED_PATH)
matches = pd.read_csv(MATCH_PATH)[["Id", "matchId"]]

# â”€â”€â”€ Merge matchId into predictions â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
df = preds.merge(matches, on="Id", how="left")

# â”€â”€â”€ Sort by predicted win placement per match â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
df_sorted = df.sort_values(["matchId", "pred_calibrated"], ascending=[True, False])

# â”€â”€â”€ Get top predicted winner per match â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
winners = df_sorted.groupby("matchId").first().reset_index()

# â”€â”€â”€ Output Sample â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
print("\nğŸ† Top Predicted Players per Match:")
print(winners[["matchId", "Id", "pred_calibrated"]].head())

# â”€â”€â”€ (Optional) Save to CSV â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
WINNER_CSV = "data/processed/predicted_match_winners.csv"
winners.to_csv(WINNER_CSV, index=False)
print(f"\nâœ” Predicted winners saved to: {WINNER_CSV}")
